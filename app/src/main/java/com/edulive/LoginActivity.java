    private void login(final String email,final String password){
        try {
            String link = getString(R.string.base_url).concat("api/login");
            Map<String, String> postParam= new HashMap<String, String>();
            postParam.put("email",email);
            postParam.put("password",password);
            JsonObjectRequest jsonObjReq = new JsonObjectRequest(Request.Method.POST, link, new JSONObject(postParam),
                    new Response.Listener<JSONObject>() {
                        @Override
                        public void onResponse(JSONObject response) {
                            try{
                                showProgress(false);
                                String status = response.getString("status");
                                System.out.println("show_error"+response.toString());
                                if(status.equals("success")){
                                    String userid = response.getString("userid");
                                    String firstname = response.getString("firstname");
                                    String lastname = response.getString("lastname");
                                    String phone = response.getString("phone");
                                    session.createSessionData(userid,firstname,lastname,email,phone);//save session outside of creating new record...
                                    //navigate to the main page
                                    Intent MainScreen = new Intent(LoginActivity.this, MainActivity.class);
                                    startActivity(MainScreen);
                                    finish();
                                } else if(status.equals("empty")){
                                    messageAlertBox("Empty Fields.", "Please enter all required fields.", "empty");
                                } else if(status.equals("failure")){
                                    messageAlertBox("Login Failed.", "Something went wrong. Please try later.", "failure");
                                    mEmailView.requestFocus();
                                }
                            } catch (Exception e) {
                                showProgress(false);
                                messageAlertBox("Error Occured.", e.getLocalizedMessage().toString(), "success");
                            }
                        }
                    }, new Response.ErrorListener() {
                @Override
                public void onErrorResponse(VolleyError error) {
                }
            }) {
                @Override
                public Map<String, String> getHeaders() throws AuthFailureError {
                    HashMap<String, String> headers = new HashMap<String, String>();
                    headers.put("Content-Type", "application/json; charset=utf-8");
                    return headers;
                }
            };
            // Adding request to request queue
            requestQueue.add(jsonObjReq);

        } catch (Exception e) {
            System.out.println("show_error "+e.getLocalizedMessage());
        }
    }

